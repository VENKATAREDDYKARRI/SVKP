public class Typecasting {

	public static void main(String[] args) {
		int i= 10;  //variables with f is a type of integer which is holding value of 10
		long l = (long)i; // defining a var with name l which of datatype long= convert value that is present in l to long
		System.out.println(l);

	}

}
